public class NoticeBean {
    String notice_author;
    String notice_title;
    String notice_time;
    String notice_head_url;

    public String getNotice_author() {
        return notice_author;
    }

    public void setNotice_author(String notice_author) {
        this.notice_author = notice_author;
    }

    public String getNotice_title() {
        return notice_title;
    }

    public void setNotice_title(String notice_title) {
        this.notice_title = notice_title;
    }

    public String getNotice_time() {
        return notice_time;
    }

    public void setNotice_time(String notice_time) {
        this.notice_time = notice_time;
    }

    public String getNotice_head_url() {
        return notice_head_url;
    }

    public void setNotice_head_url(String notice_head_url) {
        this.notice_head_url = notice_head_url;
    }
}
